#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list *last_element;

	last_element = lst;
	if (!lst)
	{
		return (0);
	}
	while (last_element->next)
	{
		last_element = last_element->next;
	}
	return (last_element);
}
